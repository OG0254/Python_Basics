# A simple program that:

# Asks the user for their name and birth year
# Calculates their current age
# Prints a short summary with their name and age

# Code

name = str(input('Enter your name: '))
birth_year = int(input('Enter your birth year: '))

current_year = 2025
birth_year = current_year - birth_year

print('\n---- Summary of name and age ---')
print(f'{name}')
print(f'{birth_year}')